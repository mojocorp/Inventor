#include <stdio.h>
#include <string.h>

#define OUT(STRING) fprintf(makefile,"%s\n",STRING)

static void
writeHeader(FILE *makefile, char *filename)
{
    OUT("#!smake");
    OUT("# Copyright (C) 1990,1991  Silicon Graphics, Inc.");
    OUT("#");
    OUT("#_______________________________________________________________________");
    OUT("#______________  S I L I C O N   G R A P H I C S   I N C .  ____________");
    OUT("#");
    OUT(filename);
    OUT("#");
    OUT("#                         ******* NOTE *******");
    OUT("#");
    OUT("#   	This file was generated by makemake - do not edit by hand.");
    OUT("#");
    OUT("#                         ******* NOTE *******");
    OUT("#");
    OUT("#______________  S I L I C O N   G R A P H I C S   I N C .  ____________");
    OUT("#_______________________________________________________________________");
    OUT("#");
}

void
MMwrite_libMakefile(FILE *makefile, char *name, char *srcdirs, char *incldirs)
{
    char filename[256];
    sprintf(filename, "#   File Name: Inventor/lib/%s/Makefile", name);

    writeHeader(makefile, filename);
    OUT(" ");
    OUT("DEPTH = ../");
    OUT("include $(DEPTH)../make/so_commondefs");
    OUT(" ");
    OUT("# specific to this library");
    fprintf(makefile, "LIBNAME    = lib%s.a\n", name);
    fprintf(makefile, "COMMONPREF = lib%s\n", name);
    fprintf(makefile, "NAME       = %s\n", name);
    fprintf(makefile, "SRCDIRS    = %s\n", srcdirs);
    fprintf(makefile, "INCLDIRS	  = %s\n", incldirs);
    OUT(" ");
    OUT("include $(SO_LIBCOMMONRULES)");
}

void
MMwrite_libSrcMakefile(FILE *makefile, char *name, char *dir, char *depth)
{
    char filename[256];
    sprintf(filename, "#   File Name: Inventor/lib/%s/%s/Makefile", name, dir);

    writeHeader(makefile, filename);
    OUT(" ");
    fprintf(makefile, "DEPTH = %s\n", depth);
    fprintf(makefile, "SOLIB = $(DEPTH)$(SHLIB_PREFIX)%s/lib%s.a\n", name,name);
    OUT("include $(DEPTH)../make/so_commondefs");
}

/* This one called before HFILES is defined */
void
MMwrite_includeMakefile(FILE *makefile, char *name, char *dir, char *depth)
{
    char filename[256];
    char cppdir[256];
    char cdir[256];
    char *s, *t;
    char oldch;

    sprintf(filename, "#   File Name: Inventor/lib/%s/%s/Makefile", name, dir);

    writeHeader(makefile, filename);
    OUT("");
    fprintf(makefile, "DEPTH = %s\n", depth);
    fprintf(makefile, "include $(DEPTH)../make/so_commondefs\n");
    fprintf(makefile, "\nHINST_DIR = %s\n", dir);
    fprintf(makefile, "ORIG_DIR = %s%s\n", depth, dir);

    s=strstr(dir, "Inventor");
    if (s==NULL) {
	fprintf(stderr, "MMwrite_includeMakefile(): Bogus!\n");
	exit(1);
    }
    s+=8;
    oldch= *s;
    *s=0;
    strcpy(cdir, dir);
    strcpy(cppdir, dir);
    strcat(cdir, "_c");
    strcat(cppdir, "_c++");
    *s=oldch;
    strcat(cdir, s);
    strcat(cppdir, s);

    fprintf(makefile, "CAPI_HINST_DIR = %s\n", cdir);
    fprintf(makefile, "CPP_DIR = %s%s\n", depth, cppdir);
    fprintf(makefile, "CAPI_DIR = %s%s\n", depth, cdir);
    fprintf(makefile, "CAPI_SOLIB = %slib%s_c.a\n\n", depth, name);
}

